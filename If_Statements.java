package com.Control_Statements;

public class If_Statements {
	public static void main(String[] args) {
		int mark = 50;
		if (mark> 50) {
			System.out.println("Secured High Marks " +mark);
			
		}
		else if (mark==50) {
			System.out.println("Secured Average Marks " +mark);
		} 
		
		else {
			System.out.println("Secured Low Marks " +mark);
		}
		

	}
	
}
