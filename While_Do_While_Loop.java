package com.Control_Statements;
public class While_Do_While_Loop {
	public static void main(String[] args) {
		int marks = 40;
		while(marks<=45) {
			System.out.println(marks);
			marks++;
		}
		System.out.println("\n");		
		int rank = 1;
		do{
			System.out.println(rank);
			rank++;
		}while(rank<=5);
	}
}


